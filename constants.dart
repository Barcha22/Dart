void main() {
  // const without datatype
  const name = "Muneeb";
  print(name);

  //const with datatype
  const String lastName = "Barcha";
  print(lastName);

  //final without datatype
  final pi = 3.14;
  print(pi);

  // final with datatype
  final double percentage = 90.6;
  print(percentage);
}
